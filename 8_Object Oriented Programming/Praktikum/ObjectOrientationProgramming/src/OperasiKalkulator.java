public class OperasiKalkulator {

    protected void penjumlahan(int a, int b){
        int isPenjumlahan = a + b ;
        System.out.println("Total penjumlahan : " + isPenjumlahan);

    }
    protected void pengurangan(int a, int b){
        int isPengurangan = a - b ;
        System.out.println("Total pengurangan : " + isPengurangan);

    }

    protected void perkalian(int a, int b){
        int isPerkalian = a * b ;
        System.out.println("Total perkalian : " + isPerkalian);

    }
    protected void pembagian(int a, int b){
        int isPembagian = a / b ;
        System.out.println("Total pembagian : " + isPembagian);

    }
}
